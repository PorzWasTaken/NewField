# Define a string
s = "Hello, World!"

# Extract the first 5 characters
print(s[:5])  # Output: Hello

# Extract characters starting from index 7
print(s[7:])  # Output: World!

# Extract characters from index 2 to 7 (exclusive)
print(s[2:7])  # Output: llo, 

# Extract every other character
print(s[::2])  # Output: Hlo ol!

# Reverse the string
print(s[::-1])  # Output: !dlroW ,olleH
